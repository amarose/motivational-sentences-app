    private val _quotes = MutableStateFlow<List<Quote>>(emptyList())
    override val quotes: StateFlow<List<Quote>> = _quotes.asStateFlow()

    init {
        CoroutineScope(Dispatchers.IO).launch {
            val quotesFromDb = quoteDao.getAllQuotes().first()
            if (quotesFromDb.isEmpty()) {
                quoteDao.insertAll(initialQuotes)
                _quotes.value = initialQuotes
            } else {
                _quotes.value = quotesFromDb
            }

            launch(Dispatchers.Main) {
                quoteDao.getAllQuotes().collect {
                    _quotes.value = it
                }
            }
        }
    }

    override suspend fun getRandomQuote(): Quote {
        if (_quotes.value.isEmpty()) {
            // Wait for the flow to emit a non-empty list to avoid race condition on startup
            _quotes.first { it.isNotEmpty() }
        }
        return _quotes.value.random()
    }

    override suspend fun updateQuote(quote: Quote) {
        quoteDao.updateQuote(quote)
    }

    override fun getFavoriteQuotes(): Flow<List<Quote>> {
        return _quotes.map { quotes ->
            quotes.filter { it.isFavorite }
        }
    }

    override suspend fun getRandomUnseenQuote(): Quote? {
        return quoteDao.getRandomUnseenQuote()
    }

    override suspend fun markQuoteAsSeen(quoteId: Int) {
        quoteDao.markQuoteAsSeen(quoteId)
    }

    override suspend fun resetAllQuotesToUnseen() {
        quoteDao.resetAllQuotesToUnseen()
    }
